// =================
//    JS Variables
// =================

// JavaScript Variables can be declared in 4 ways:
// 1) Automatically
// 2) Using var
// 3) Using let
// 4) Using const

// ðŸ‘‰ Automatically
x = 10;
y = 20;
z = x + y;
console.log(z);

// ðŸ‘‰ Using var
var a = 5;
var b = 6;
var c = a + b;
console.log(c);

// ðŸ‘‰ Using let
let l = 2;
let m = 3;
let n = l + m;
console.log(n);

// ðŸ‘‰ Using const
const p = 5;
const q = 10;
const r = p + q;
console.log(r);

// ===========================
//  JavaScript Dollar Sign $
// ===========================

// Since JavaScript treats a dollar sign as a letter, identifiers containing $ are valid variable names
let $ = "Hello World";
let $$$ = 2;
let $myMoney = 5;
console.log($$$);

// ===========================
//  JavaScript Underscore (_)
// ===========================

// Since JavaScript treats underscore as a letter, identifiers containing _ are valid variable names
let _lastName = "Johnson";
let _x = 2;
let _100 = 5;
console.log(_100);