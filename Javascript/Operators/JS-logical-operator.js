// ðŸ‘‰ Logical AND (&&)
let a = true;  
let b = false;  
console.log(a && b); // Output: false  
console.log(a && true); // Output: true

// ðŸ‘‰ Logical OR (||)
let x = true;  
let y = false;  
console.log(x || y); // Output: true  
console.log(y || false); // Output: false 

// ðŸ‘‰ Logical NOT (!)
let isTrue = true;  
console.log(!isTrue); // Output: false  
console.log(!false);  // Output: true  